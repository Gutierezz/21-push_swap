#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *ptr;

	ptr = (char*)s;
	while (1)
	{
		if (*ptr == c)
			return (ptr);
		if (*ptr == '\0')
			return (NULL);
		ptr++;
	}
}
