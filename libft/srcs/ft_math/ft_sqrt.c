#include "libft.h"
#include "ft_math.h"

int		ft_sqrt(int nb)
{
	int n;

	n = 0;
	while (nb > 0)
	{
		++n;
		nb -= (2 * n - 1);
	}
	if (nb == 0)
		return (n);
	else
		return (0);
}
